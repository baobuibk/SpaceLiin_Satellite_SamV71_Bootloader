/*
 * xtp_crc_table.c 
 * Description:
 *   Contains the 256-entry const lookup tables used when XTP_CRC_USE_TABLE=1.
 *   Marked `const` (no RAM cost at runtime).
 *
 * Generation algorithm for CRC-32 (MSB-first, poly 0x04C11DB7):
 *   for i in range(256):
 *       crc = i << 24
 *       for _ in range(8):
 *           if crc & 0x80000000: crc = (crc << 1) ^ 0x04C11DB7
 *           else:                crc <<= 1
 *           crc &= 0xFFFFFFFF
 *       table[i] = crc
 *
 * Generation algorithm for CRC-16/XMODEM (MSB-first, poly 0x1021):
 *   for i in range(256):
 *       crc = i << 8
 *       for _ in range(8):
 *           if crc & 0x8000: crc = (crc << 1) ^ 0x1021
 *           else:            crc <<= 1
 *           crc &= 0xFFFF
 *       table[i] = crc
 *                                                  C.H
 */

